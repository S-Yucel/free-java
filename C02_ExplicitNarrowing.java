package ders04_dataCasting;

public class C02_ExplicitNarrowing {
    public static void main(String[] args) {
        int a = 135;
        byte b = (byte) a;

        System.out.println(b);

        /*
        Daha geniş kapsamlı sayısal data türünden daha az kapsamlı bir data türüne
        explicit narrowing ile casting yapmak istersek,
        Java çevirmek istediğimiz data türünün sınırları içerisinde tekrar tekrar
        dönecektir.

         */

    }
}
